public class Main {
    public static void main(String[] args) {

        // Для цикла for
        for (int i = 200000; i <= 210000; i++) {
            System.out.println(i);
        }
        for (int i = 220000; i <= 235000; i++) {
            System.out.println(i);
        }

        // Для цикла while
        int i = 200000;
        while (i <= 210000) {
            System.out.println(i);
            i++;
        }
        i = 220000;
        while (i <= 235000) {
            System.out.println(i);
            i++;
        }

    }
}
